export function buildUserPrompt(data: PageData): string {
  const lines: string[] = [];

  lines.push(`URL: ${data.url}`);
  lines.push("");

  if (data.desktop) {
    lines.push("=== ДЕСКТОПНАЯ ВЕРСИЯ ===");
    if (data.desktop.title) lines.push(`Title: ${data.desktop.title}`);
    if (data.desktop.description) {
      lines.push(`Description: ${data.desktop.description}`);
    }
    if (data.desktop.h1) lines.push(`H1: ${data.desktop.h1}`);
    if (typeof data.desktop.body_words === "number") {
      lines.push(`Слов в body: ${data.desktop.body_words}`);
    }
    if (typeof data.desktop.forms_count === "number") {
      lines.push(`Форм на странице: ${data.desktop.forms_count}`);
    }
    if (data.desktop.contacts?.tel?.length) {
      lines.push(`Телефоны: ${data.desktop.contacts.tel.join(", ")}`);
    }
    if (data.desktop.contacts?.mailto?.length) {
      lines.push(`Email: ${data.desktop.contacts.mailto.join(", ")}`);
    }
    lines.push("");
  }

  if (data.mobile) {
    lines.push("=== МОБИЛЬНАЯ ВЕРСИЯ ===");
    if (data.mobile.title) lines.push(`Title: ${data.mobile.title}`);
    if (data.mobile.description) {
      lines.push(`Description: ${data.mobile.description}`);
    }
    if (data.mobile.h1) lines.push(`H1: ${data.mobile.h1}`);
    if (typeof data.mobile.body_words === "number") {
      lines.push(`Слов в body: ${data.mobile.body_words}`);
    }
    if (typeof data.mobile.forms_count === "number") {
      lines.push(`Форм на странице: ${data.mobile.forms_count}`);
    }
    lines.push("");
  }

  if (data.trust_notes?.length) {
    lines.push("=== БАЗОВЫЕ ПРОВЕРКИ ===");
    for (const note of data.trust_notes) {
      lines.push(`- ${note}`);
    }
    lines.push("");
  }

  lines.push("=== ЗАДАЧА ===");
  lines.push(
    "Посмотри скриншоты выше и текстовые данные. Дай разбор как Site Doctor по формату из системного промпта. Верни только JSON."
  );

  return lines.join("\n");
}
